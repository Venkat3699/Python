a = 5.0
b = 2.5

# Division
result = a / b
print("Division of a and b is:", result)

# Modulus (Remainder)
result1 = a % b
print("Modulus of a and b is:", result1)

# Absolute Value
result2 = abs(-7)
print("Absolute value of (-7) is:", result2)

# Output is:
# Division of a and b is: 2.0
# Modulus of a and b is: 0.0
# Absolute value of (-7) is: 7